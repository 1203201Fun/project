package com.entity;

public class Addinfo {
	String tpname;
	String tpadress;
	String tpdetail;
	String tpnum;
	String kind;
	public String getTpname() {
		return tpname;
	}
	public void setTpname(String tpname) {
		this.tpname = tpname;
	}
	public String getTpadress() {
		return tpadress;
	}
	public void setTpadress(String tpadress) {
		this.tpadress = tpadress;
	}
	public String getTpdetail() {
		return tpdetail;
	}
	public void setTpdetail(String tpdetail) {
		this.tpdetail = tpdetail;
	}
	public String getTpnum() {
		return tpnum;
	}
	public void setTpnum(String tpnum) {
		this.tpnum = tpnum;
	}
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
}
