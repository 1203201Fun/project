package com.entity;

public class Stock {
	String devname;
	String devadress;
	String devperson;
	String devnum;
	String devdetail;
	String devflag;
	String kind;
	public String getDevname() {
		return devname;
	}
	public void setDevname(String devname) {
		this.devname = devname;
	}
	public String getDevadress() {
		return devadress;
	}
	public void setDevadress(String devadress) {
		this.devadress = devadress;
	}
	public String getDevperson() {
		return devperson;
	}
	public void setDevperson(String devperson) {
		this.devperson = devperson;
	}
	public String getDevnum() {
		return devnum;
	}
	public void setDevnum(String devnum) {
		this.devnum = devnum;
	}
	public String getDevdetail() {
		return devdetail;
	}
	public void setDevdetail(String devdetail) {
		this.devdetail = devdetail;
	}
	public String getDevflag() {
		return devflag;
	}
	public void setDevflag(String devflag) {
		this.devflag = devflag;
	}
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
}
